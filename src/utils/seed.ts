/**
 * Seed-based pseudo-random number generator.
 * Uses a simple mulberry32 algorithm for deterministic random numbers.
 */
export function createSeededRandom(seed: number): () => number {
  return function () {
    seed |= 0
    seed = (seed + 0x6d2b79f5) | 0
    let t = Math.imul(seed ^ (seed >>> 15), 1 | seed)
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296
  }
}

/**
 * Generate a deterministic seed from a date string (YYYY-MM-DD).
 */
export function dateSeed(date: Date = new Date()): number {
  const dateStr = date.toISOString().slice(0, 10)
  let hash = 0
  for (let i = 0; i < dateStr.length; i++) {
    const char = dateStr.charCodeAt(i)
    hash = ((hash << 5) - hash + char) | 0
  }
  return Math.abs(hash)
}
